        private async Task RunInstallation()
        {
            UpdateProgress("Creating directories...", $"Creating {Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData), "Simple")}");
            await Task.Delay(100);

            string installDir = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData), "Simple");
            string binDir = Path.Combine(installDir, "bin");
            string examplesDir = Path.Combine(installDir, "examples");
            string startMenuDir = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.ApplicationData),
                @"Microsoft\Windows\Start Menu\Programs\Simple Programming Language");

            Directory.CreateDirectory(installDir);
            Directory.CreateDirectory(binDir);
            Directory.CreateDirectory(examplesDir);
            Directory.CreateDirectory(startMenuDir);

            UpdateProgress("Installing Simple executable...", $"Copying simple.exe to {binDir}");
            File.Copy(_simpleExe, Path.Combine(binDir, "simple.exe"), true);
            await Task.Delay(100);

            UpdateProgress("Installing examples...", $"Copying example programs to {examplesDir}");
            if (Directory.Exists(_examplesDir))
            {
                foreach (string file in Directory.GetFiles(_examplesDir, "*.spml"))
                {
                    File.Copy(file, Path.Combine(examplesDir, Path.GetFileName(file)), true);
                }
            }
            await Task.Delay(100);

            UpdateProgress("Creating uninstaller...", "Writing uninstall script");
            string uninstallScript = CreateUninstallScript(installDir, binDir, startMenuDir);
            File.WriteAllText(Path.Combine(installDir, "uninstall.ps1"), uninstallScript);
            await Task.Delay(100);

            UpdateProgress("Adding to PATH...", "Updating user PATH environment variable");
            AddToUserPath(binDir);
            await Task.Delay(100);

            UpdateProgress("Creating shortcuts...", "Adding Start Menu entries");
            CreateShortcuts(binDir, installDir, startMenuDir);
            await Task.Delay(100);

            UpdateProgress("Registering application...", "Writing uninstall registry keys");
            WriteRegistryKeys(installDir);
        }

