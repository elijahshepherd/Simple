            Directory.CreateDirectory(tempDir);

            try
            {

                string simpleExe = Path.Combine(tempDir, "simple.exe");
                ExtractResource("SimpleInstaller.simple.exe", simpleExe);


                string examplesDir = Path.Combine(tempDir, "examples");
                Directory.CreateDirectory(examplesDir);
                ExtractExamples(tempDir);


                string ps1Path = Path.Combine(tempDir, "install.ps1");
                CreateInstallerScript(ps1Path, tempDir);


                Application.Run(new InstallerForm(simpleExe, examplesDir, ps1Path));
            }
            finally
            {

                try { Directory.Delete(tempDir, true); } catch { }
            }
        }

        static void ExtractResource(string resourceName, string outputPath)
        {
            Assembly asm = Assembly.GetExecutingAssembly();
            using (Stream stream = asm.GetManifestResourceStream(resourceName))
            {
                if (stream == null)
                {

                    string[] names = asm.GetManifestResourceNames();
                    foreach (string n in names)
                    {
                        if (n.EndsWith("simple.exe", StringComparison.OrdinalIgnoreCase))
                        {
                            stream = asm.GetManifestResourceStream(n);
                            break;
                        }
                    }
                }

                if (stream != null)
                {
                    using (FileStream fs = new FileStream(outputPath, FileMode.Create))
                    {
                        stream.CopyTo(fs);
                    }
                }
                else
                {
                    throw new Exception($"Resource not found: {resourceName}. Available: {string.Join(", ", asm.GetManifestResourceNames())}");
                }
            }
        }

        static void ExtractExamples(string tempDir)
        {
            Assembly asm = Assembly.GetExecutingAssembly();
            string[] names = asm.GetManifestResourceNames();
            string examplesDir = Path.Combine(tempDir, "examples");

            foreach (string name in names)
            {
                if (name.EndsWith(".spml", StringComparison.OrdinalIgnoreCase) && name.Contains("examples."))
                {
                    string fileName = Path.GetFileName(name);
                    string outputPath = Path.Combine(examplesDir, fileName);
                    using (Stream stream = asm.GetManifestResourceStream(name))
                    using (FileStream fs = new FileStream(outputPath, FileMode.Create))
                    {
                        stream.CopyTo(fs);
                    }
                }
            }
        }

