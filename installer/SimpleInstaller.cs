        private void AddToUserPath(string binDir)
        {
            string currentPath = Environment.GetEnvironmentVariable("PATH", EnvironmentVariableTarget.User);
            if (!currentPath.Contains(binDir))
            {
                string newPath = currentPath + ";" + binDir;
                Environment.SetEnvironmentVariable("PATH", newPath, EnvironmentVariableTarget.User);


                NativeMethods.SendMessageTimeout(
                    (IntPtr)0xFFFF, 0x001A, UIntPtr.Zero, "Environment",
                    0x0002, 5000, out UIntPtr _);
            }
        }

        private void CreateShortcuts(string binDir, string installDir, string startMenuDir)
        {
            Type shellType = Type.GetTypeFromProgID("WScript.Shell");
            dynamic shell = Activator.CreateInstance(shellType);


            dynamic shortcut = shell.CreateShortcut(Path.Combine(startMenuDir, "Simple.lnk"));
            shortcut.TargetPath = Path.Combine(binDir, "simple.exe");
            shortcut.WorkingDirectory = binDir;
            shortcut.Description = "Simple Programming Language";
            shortcut.Save();


            shortcut = shell.CreateShortcut(Path.Combine(startMenuDir, "Uninstall.lnk"));
            shortcut.TargetPath = "powershell.exe";
            shortcut.Arguments = $"-ExecutionPolicy Bypass -File \"{Path.Combine(installDir, "uninstall.ps1")}\"";
            shortcut.WorkingDirectory = installDir;
            shortcut.Description = "Uninstall Simple Programming Language";
            shortcut.Save();


            shortcut = shell.CreateShortcut(Path.Combine(startMenuDir, "Documentation.lnk"));
            shortcut.TargetPath = "https://github.com/elijahshepherd/Simple";
            shortcut.Description = "Simple Programming Language Documentation";
            shortcut.Save();
        }

        private void WriteRegistryKeys(string installDir)
        {
            string regPath = @"Software\Microsoft\Windows\CurrentVersion\Uninstall\Simple";
            using (var key = Microsoft.Win32.Registry.CurrentUser.CreateSubKey(regPath))
            {
                key.SetValue("DisplayName", "Simple Programming Language");
                key.SetValue("DisplayVersion", "1.5.0");
                key.SetValue("Publisher", "Elijah Shepherd");
                key.SetValue("URLInfoAbout", "https://github.com/elijahshepherd/Simple");
                key.SetValue("UninstallString", $"powershell.exe -ExecutionPolicy Bypass -File \"{Path.Combine(installDir, "uninstall.ps1")}\"");
                key.SetValue("NoModify", 1, Microsoft.Win32.RegistryValueKind.DWord);
                key.SetValue("NoRepair", 1, Microsoft.Win32.RegistryValueKind.DWord);
            }

            string ourRegPath = @"Software\Simple";
            using (var key = Microsoft.Win32.Registry.CurrentUser.CreateSubKey(ourRegPath))
            {
                key.SetValue("InstallDir", installDir);
                key.SetValue("Version", "1.5.0");
            }
        }

